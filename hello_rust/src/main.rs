// Entry point of the Rust program
// Every executable Rust application starts running from the `main` function
fn main() {
    // Print a message to the console
     // `println!` is a macro in Rust that allows you to print text to the console.
    // It prints formatted text to the console, followed by a new line
    // This line outputs a welcome message to the user
    println!("Hello Gloria , Welcome to rust world!");
    // The `main` function does not return any value, so its return type is `()`, which is the unit type in Rust.
}
